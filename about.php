                    During the course of Blacky’s recovery, miracles were beginning to happen in Sarah and David’s home. One time, David brought home three little felines. It turns out he found them abandoned somewhere, and he couldn’t help but respond to their cries for help. Sarah also had similar experiences. Whenever she walked home from work, she would notice other dogs and cats needing treatment for their wounds. These had no home, no healthy food, and no protection whatsoever from potential abuse.<br><br>


                    Soon, Sarah and David found themselves rescuing and adopting more and more of these animals.<br><br>


